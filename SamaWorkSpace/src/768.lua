						local 是否重复 = false
						for j=1,#个人突破可选位置 do
							if 个人突破可选位置[j] == i then
								是否重复= true
								break
							end
						end
						if 是否重复 == false then
							table.insert(个人突破可选位置,i)
						end
					else
						table.insert(个人突破可选位置,i)
					end
				end
			end
		end
		if #个人突破失败位置 ~= 0 then
			for j=#个人突破失败位置,1,-1 do
				for var = 1,#个人突破可选位置 do
					if 个人突破失败位置[j] == 个人突破可选位置[var] then
						table.remove(个人突破可选位置,var)
						break
					end
				end
			end
		end
		local temp_str = ""
		for var=1,#个人突破可选位置 do
			sysLog(个人突破可选位置[var])
			temp_str = temp_str..":"..var
		end
		if #个人突破可选位置 ~= 0 then
			showHD_b("勋章"..rtype..",可选位置"..temp_str)
			local rand = math.random(1,#个人突破可选位置)
			上次突破选择位置 = 个人突破可选位置[rand]
